package com.dotmarketing.portlets.contentlet.business;

public class BinaryContentExporterException extends Exception {

	private static final long serialVersionUID = 1L;

	public BinaryContentExporterException() {
	}

	public BinaryContentExporterException(String message) {
		super(message);
	}

	public BinaryContentExporterException(Throwable cause) {
		super(cause);
	}

	public BinaryContentExporterException(String message, Throwable cause) {
		super(message, cause);
	}

}
