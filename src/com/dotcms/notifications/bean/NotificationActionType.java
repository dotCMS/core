package com.dotcms.notifications.bean;

/**
 * Notification Action objects allow developers to specify additional
 * information or features to an existing Notification object. This
 * {@code NotificationActionType} class allows developers to specify the type of
 * action to display and handle the Notification fields and attributes
 * accordingly. For example, a Notification Type can be a link, a button, an
 * image, etc.
 * 
 * @author Jose Castro
 * @version 3.7
 * @since Jul 12, 2016
 *
 */
public enum NotificationActionType {

	LINK, BUTTON, IMAGE

}
