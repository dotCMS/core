package com.dotcms.publisher.business;

public class DotSolrException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DotSolrException() {
		super();

	}

	public DotSolrException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public DotSolrException(String arg0) {
		super(arg0);

	}

	public DotSolrException(Throwable arg0) {
		super(arg0);

	}

	public String getMessage() {
		return super.getMessage();
	}
	
	
	
	
	
	
}
